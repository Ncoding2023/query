CREATE TABLE enrollment(
    NO NUMBER NOT NULL,
    e_year CHAR(4) NOT NULL,
    e_semester NVARCHAR2(4) NOT NULL,
    sd_num CHAR(8) NOT NULL,
    c_NuM CHAR(7) NOT NULL,         --과목
    e_grade NUMBER(2,1) NOT NULL,   --성적
    e_DATE DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT enrollment_no_pk PRIMARY KEY(no),
    CONSTRAINT enrollment_e_semester_ck CHECK (e_semester IN ('1학기','2학기','3학기' )),
    CONSTRAINT enrollment_e_grade_ck CHECK (e_grade BETWEEN 0 AND 4.5),
    CONSTRAINT enrollment_sd_num_fk FOREIGN KEY (sd_num) REFERENCES student(sd_num),
    CONSTRAINT enrollment_c_num_fk FOREIGN KEY (c_num) REFERENCES course(c_num)
);

CREATE SEQUENCE enrollment_seq
START WITH 1
INCREMENT BY 1
MINVALUE 1
MAXVALUE 99999
NOCYCLE
CACHE 2;

