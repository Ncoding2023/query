CREATE TABLE course (
no NUMBER NOT NULL,
c_num CHAR (7) NOT NULL,
c_name VARCHAR2 (80)NOT NULL,
c_credit NUMBER (2) NOT NULL,
c_section NCHAR(2) NOT NULL,
CONSTRAINT course_no_pk PRIMARY KEY (no),
CONSTRAINT course_c_num_uk UNIQUE (c_num),
CONSTRAINT course_c_credit_ck CHECK (c_credit BETWEEN 2 AND 4),
CONSTRAINT course_c_section_ck CHECK (c_section IN ('교양','일반','전공' ))
);
CREATE SEQUENCE course_seq
START WITH 1
INCREMENT BY 1
MINVALUE 1
MAXVALUE 99999
NOCYCLE
CACHE 2;

